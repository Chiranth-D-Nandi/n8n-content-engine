def list_genres() -> list[dict]:
    """Trimmed list for backwards compatibility (main.py menu display)."""
    return [
        {
            'id': g['id'],
            'name': g['name'],
            'niche': g['niche'],
            'reel_style': g['reel_style'],
            'duration': f"{g['duration_seconds']}s"
        }
        for g in GENRE_LIBRARY.values()
    ]


def list_genres_full() -> list[dict]:
    """
    FIX: Full genre data for the dashboard /genres endpoint.
    The dashboard merges these with config channels and renders
    fields like search_keywords, content_rules, voice_preset etc.
    Returning trimmed data caused the channel info panel to show
    nothing and broke onChannelChange() silently.
    """
    return [g.copy() for g in GENRE_LIBRARY.values()]


def get_genre(genre_id: str) -> dict:
    """Get full genre config by ID."""
    if genre_id not in GENRE_LIBRARY:
        available = list(GENRE_LIBRARY.keys())
        raise ValueError(
            f"Genre '{genre_id}' not found. Available: {available}"
        )
    return GENRE_LIBRARY[genre_id].copy()


def get_genres_by_ids(genre_ids: list[str]) -> list[dict]:
    """Get multiple genres for batch processing."""
    return [get_genre(gid) for gid in genre_ids]
